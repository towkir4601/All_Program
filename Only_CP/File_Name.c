#include <stdio.h>
#include<string.h>
int main() {
    int n;
    // ১. স্ট্রিংয়ের দৈর্ঘ্য ইনপুট নেওয়া
    scanf("%d", &n);

    // ২. স্ট্রিংটি ইনপুট নেওয়া (constraints অনুযায়ী সর্বোচ্চ ১০০ অক্ষর হতে পারে)
    char s[105];
    scanf("%s", s);

    int ans = 0;

    // ৩. লুপ চালিয়ে চেক করা
    // আমরা n-2 পর্যন্ত লুপ চালাব যাতে s[i+2] এক্সেস করার সময় মেমোরি এরর না হয়
    for (int i = 0; i < n; i++) {
        // যদি পর পর তিনটি অক্ষর 'x' হয়
        if (s[i] == 'x' && s[i+1] == 'x' && s[i+2] == 'x') {
            ans++;
        }
    }

    // ৪. উত্তর প্রিন্ট করা
    printf("%d\n", ans);

    return 0;
}